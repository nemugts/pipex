#include <stdio.h>

int main() {
    // 文字列の出力
    printf("Hello, World!\n");

    // 整数の出力
    int num = 42;
    printf("The number is: %d\n", num);

    // 浮動小数点数の出力
    float f = 3.14159;
    printf("The value of pi is approximately: %.2f\n", f);

    // 文字の出力
    char ch = 'A';
    printf("The character is: %c\n", ch);

    return 0;
    //thx
}
